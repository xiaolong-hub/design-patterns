package creation.Builder;

public class Cap {
    private String capTop;

    private String capInner;

    private String capOut;

    public String getCapTop() {
        return capTop;
    }

    public String getCapInner() {
        return capInner;
    }

    public String getCapOut() {
        return capOut;
    }

    public void setCapTop(String capTop) {
        this.capTop = capTop;
    }

    public void setCapInner(String capInner) {
        this.capInner = capInner;
    }

    public void setCapOut(String capOut) {
        this.capOut = capOut;
    }

}
